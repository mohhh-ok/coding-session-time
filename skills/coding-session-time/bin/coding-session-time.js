function parseDuration(input, fallbackUnit = "s") {
  const m = input.trim().match(/^(\d+(?:\.\d+)?)([smh]?)$/i);
  if (!m)
    throw new Error(`invalid duration: ${input}`);
  const n = Number(m[1]);
  const unit = (m[2] || fallbackUnit).toLowerCase();
  return unit === "h" ? n * 3600 : unit === "m" ? n * 60 : n;
}
function isRealUserPrompt(content) {
  if (typeof content === "string")
    return true;
  if (Array.isArray(content) && content.length > 0) {
    const first = content[0];
    return !!first && first.type !== "tool_result";
  }
  return false;
}
function loadEventsFromProjects(dir, opts) {
  const out = [];
  let subs;
  try {
    subs = readdirSync(dir);
  } catch {
    return out;
  }
  const sinceCutoffSec = opts?.since != null ? Date.parse(opts.since + "T00:00:00Z") / 1000 - 86400 : -Infinity;
  const tz = opts?.tz ?? "UTC";
  const since = opts?.since;
  const until = opts?.until;
  for (const sub of subs) {
    const subPath = join(dir, sub);
    let files;
    try {
      files = readdirSync(subPath).filter((f) => f.endsWith(".jsonl"));
    } catch {
      continue;
    }
    for (const f of files) {
      const filePath = join(subPath, f);
      try {
        const st = statSync(filePath);
        if (st.mtimeMs / 1000 < sinceCutoffSec)
          continue;
      } catch {
        continue;
      }
      let raw;
      try {
        raw = readFileSync(filePath, "utf8");
      } catch {
        continue;
      }
      const sessionEvents = [];
      let project = null;
      for (const line of raw.split(`
`)) {
        if (!line)
          continue;
        let r;
        try {
          r = JSON.parse(line);
        } catch {
          continue;
        }
        if (project === null && typeof r.cwd === "string")
          project = r.cwd;
        if (r.type !== "user" && r.type !== "assistant")
          continue;
        if (typeof r.timestamp !== "string")
          continue;
        const ts = Date.parse(r.timestamp) / 1000;
        if (!Number.isFinite(ts))
          continue;
        if (since || until) {
          const d = dateKey(ts, tz);
          if (since && d < since)
            continue;
          if (until && d > until)
            continue;
        }
        const isUserPrompt = r.type === "user" && isRealUserPrompt(r.message?.content);
        sessionEvents.push({ ts, isUserPrompt });
      }
      if (!project)
        continue;
      for (const e of sessionEvents)
        out.push({ ts: e.ts, project, isUserPrompt: e.isUserPrompt });
    }
  }
  out.sort((a, b) => a.ts - b.ts);
  return out;
}
function listJsonlFilesRecursive(dir) {
  const out = [];
  let entries;
  try {
    entries = readdirSync(dir);
  } catch {
    return out;
  }
  for (const entry of entries) {
    const p = join(dir, entry);
    let st;
    try {
      st = statSync(p);
    } catch {
      continue;
    }
    if (st.isDirectory())
      out.push(...listJsonlFilesRecursive(p));
    else if (entry.endsWith(".jsonl"))
      out.push(p);
  }
  return out;
}
function inRange(ts, since, until, tz) {
  if (!since && !until)
    return true;
  const d = dateKey(ts, tz);
  return (!since || d >= since) && (!until || d <= until);
}
function isCodexActivityEvent(r) {
  const payloadType = r.payload?.type;
  if (r.type === "event_msg") {
    if (payloadType === "user_message")
      return { include: true, isUserPrompt: true };
    if (payloadType === "agent_message" || payloadType === "exec_command_end" || payloadType === "task_started") {
      return { include: true, isUserPrompt: false };
    }
    return { include: false, isUserPrompt: false };
  }
  if (r.type !== "response_item")
    return { include: false, isUserPrompt: false };
  if (payloadType === "message")
    return { include: r.payload?.role === "assistant", isUserPrompt: false };
  if (payloadType === "reasoning" || payloadType === "function_call" || payloadType === "function_call_output") {
    return { include: true, isUserPrompt: false };
  }
  return { include: false, isUserPrompt: false };
}
function loadEventsFromCodexSessions(dir, opts) {
  const out = [];
  const sinceCutoffSec = opts?.since != null ? Date.parse(opts.since + "T00:00:00Z") / 1000 - 86400 : -Infinity;
  const tz = opts?.tz ?? "UTC";
  const since = opts?.since;
  const until = opts?.until;
  for (const filePath of listJsonlFilesRecursive(dir)) {
    try {
      const st = statSync(filePath);
      if (st.mtimeMs / 1000 < sinceCutoffSec)
        continue;
    } catch {
      continue;
    }
    let raw;
    try {
      raw = readFileSync(filePath, "utf8");
    } catch {
      continue;
    }
    const sessionEvents = [];
    let project = null;
    for (const line of raw.split(`
`)) {
      if (!line)
        continue;
      let r;
      try {
        r = JSON.parse(line);
      } catch {
        continue;
      }
      if (project === null && r.type === "session_meta" && typeof r.payload?.cwd === "string") {
        project = r.payload.cwd;
      }
      if (typeof r.timestamp !== "string")
        continue;
      const ts = Date.parse(r.timestamp) / 1000;
      if (!Number.isFinite(ts) || !inRange(ts, since, until, tz))
        continue;
      const activity = isCodexActivityEvent(r);
      if (activity.include)
        sessionEvents.push({ ts, isUserPrompt: activity.isUserPrompt });
    }
    if (!project)
      continue;
    for (const e of sessionEvents)
      out.push({ ts: e.ts, project, isUserPrompt: e.isUserPrompt });
  }
  out.sort((a, b) => a.ts - b.ts);
  return out;
}
var CLAUDE_WORKTREE_MARKER = "/.claude/worktrees/";
function resolveWorktreeRoot(path) {
  const marker = path.indexOf(CLAUDE_WORKTREE_MARKER);
  if (marker !== -1)
    return path.slice(0, marker);
  let out;
  try {
    out = execFileSync("git", ["-C", path, "rev-parse", "--path-format=absolute", "--git-dir", "--git-common-dir"], { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] });
  } catch {
    return path;
  }
  const [gitDir, commonDir] = out.split(`
`).map((s) => s.trim());
  if (!gitDir || !commonDir || gitDir === commonDir)
    return path;
  return dirname(commonDir);
}
function groupWorktrees(events, resolve = resolveWorktreeRoot) {
  const cache = new Map;
  for (const e of events) {
    let root = cache.get(e.project);
    if (root === undefined) {
      root = resolve(e.project);
      cache.set(e.project, root);
    }
    e.project = root;
  }
  return events;
}
function dateKey(ts, tz) {
  return new Date(ts * 1000).toLocaleDateString("en-CA", { timeZone: tz });
}
function aggregate(events, idleSec, tailSec, tz) {
  const buckets = new Map;
  for (const e of events) {
    const d = dateKey(e.ts, tz);
    const key = `${d}	${e.project}`;
    let b = buckets.get(key);
    if (!b) {
      b = { date: d, project: e.project, tss: [], promptCount: 0 };
      buckets.set(key, b);
    }
    b.tss.push(e.ts);
    if (e.isUserPrompt)
      b.promptCount++;
  }
  const rows = [];
  for (const b of buckets.values()) {
    b.tss.sort((a, b2) => a - b2);
    let secs = 0;
    for (let i = 1;i < b.tss.length; i++) {
      const gap = b.tss[i] - b.tss[i - 1];
      secs += gap <= idleSec ? gap : tailSec;
    }
    secs += tailSec;
    rows.push({ date: b.date, project: b.project, prompts: b.promptCount, seconds: secs });
  }
  return rows;
}
function todayInTz(tz) {
  return new Date().toLocaleDateString("en-CA", { timeZone: tz });
}
function shiftDate(ymd, days) {
  const [y, m, d] = ymd.split("-").map(Number);
  const dt = new Date(Date.UTC(y, m - 1, d));
  dt.setUTCDate(dt.getUTCDate() + days);
  return dt.toISOString().slice(0, 10);
}
function weekdayIndex(ymd) {
  const [y, m, d] = ymd.split("-").map(Number);
  const dow = new Date(Date.UTC(y, m - 1, d)).getUTCDay();
  return (dow + 6) % 7;
}
function resolveRange(opts, today) {
  if (opts.today)
    return { since: today, until: today };
  if (opts.yesterday) {
    const y = shiftDate(today, -1);
    return { since: y, until: y };
  }
  if (opts.thisWeek) {
    const since = shiftDate(today, -weekdayIndex(today));
    return { since, until: today };
  }
  if (opts.lastWeek) {
    const thisMon = shiftDate(today, -weekdayIndex(today));
    return { since: shiftDate(thisMon, -7), until: shiftDate(thisMon, -1) };
  }
  if (opts.thisMonth) {
    const since = today.slice(0, 8) + "01";
    return { since, until: today };
  }
  if (opts.lastMonth) {
    const firstThis = today.slice(0, 8) + "01";
    const lastPrev = shiftDate(firstThis, -1);
    const sincePrev = lastPrev.slice(0, 8) + "01";
    return { since: sincePrev, until: lastPrev };
  }
  if (opts.since || opts.until) {
    return { since: opts.since ?? "0000-01-01", until: opts.until ?? today };
  }
  const days = opts.days ?? 14;
  return { since: shiftDate(today, -(days - 1)), until: today };
}
function fmtDuration(s) {
  s = Math.floor(s);
  const h = Math.floor(s / 3600);
  const m = Math.floor(s % 3600 / 60);
  return h > 0 ? `${String(h).padStart(2)}h${String(m).padStart(2, "0")}m` : `   ${String(m).padStart(2)}m`;
}
function shortenPath(p, width = 40) {
  const home = homedir();
  const name = p.startsWith(home) ? "~" + p.slice(home.length) : p;
  return name.length > width ? "…" + name.slice(-(width - 1)) : name;
}
var WEEKDAYS = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"];
function printText(rows, opts) {
  const projTotals = new Map;
  if (!opts.total) {
    rows.sort((a, b) => a.date === b.date ? b.seconds - a.seconds : a.date < b.date ? -1 : 1);
    let curDate = null;
    let dayTotal = 0;
    for (const r of rows) {
      if (r.date !== curDate) {
        if (curDate !== null) {
          console.log(`  ${"─".repeat(60)}`);
          console.log(`  ${"Total".padEnd(40)} ${fmtDuration(dayTotal).padStart(8)}`);
          console.log("");
        }
        curDate = r.date;
        dayTotal = 0;
        const wd = WEEKDAYS[weekdayIndex(r.date)];
        console.log(import_picocolors.default.bold(`━━ ${r.date} (${wd}) ━━`));
      }
      console.log(`  ${shortenPath(r.project).padEnd(40)} ${import_picocolors.default.cyan(fmtDuration(r.seconds).padStart(8))}  ${import_picocolors.default.dim(`(${r.prompts} prompts)`)}`);
      dayTotal += r.seconds;
      projTotals.set(r.project, (projTotals.get(r.project) ?? 0) + r.seconds);
    }
    if (curDate !== null) {
      console.log(`  ${"─".repeat(60)}`);
      console.log(`  ${"Total".padEnd(40)} ${fmtDuration(dayTotal).padStart(8)}`);
      console.log("");
    }
  } else {
    for (const r of rows) {
      projTotals.set(r.project, (projTotals.get(r.project) ?? 0) + r.seconds);
    }
  }
  const sorted = [...projTotals.entries()].sort((a, b) => b[1] - a[1]);
  const limited = opts.top ? sorted.slice(0, opts.top) : sorted;
  console.log(import_picocolors.default.bold(`━━━━ ${opts.rangeLabel} — Project totals ━━━━`));
  let grand = 0;
  for (const [proj, secs] of limited) {
    console.log(`  ${shortenPath(proj).padEnd(40)} ${import_picocolors.default.cyan(fmtDuration(secs).padStart(8))}`);
    grand += secs;
  }
  if (opts.top && sorted.length > limited.length) {
    const rest = sorted.slice(limited.length).reduce((a, [, s]) => a + s, 0);
    const label = `+${sorted.length - limited.length} more`.padEnd(40);
    console.log(`  ${import_picocolors.default.dim(label)} ${import_picocolors.default.dim(fmtDuration(rest).padStart(8))}`);
    grand += rest;
  }
  console.log(`  ${"─".repeat(60)}`);
  console.log(`  ${"Grand total".padEnd(40)} ${import_picocolors.default.bold(fmtDuration(grand).padStart(8))}`);
}
